    private void DisplayPropertyCS(int id_cs) {
        Connection conn = null;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/community_center", "root", "");
            String querry = "SELECT c.id_csp, c.id_cs, p.nama_property, c.jumlah "
                    + "FROM coworking_space_properties c "
                    + "JOIN properties p USING(id_property) "
                    + "WHERE id_cs=?;";
            PreparedStatement pstmt = conn.prepareStatement(querry);
            pstmt.setInt(1, id_cs);
            ResultSet rs = pstmt.executeQuery();
            tableCSP.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Koneksi ke Database Gagal.\n" + e);
        }
    }

