    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/community_center", "root", "");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Koneksi ke Database Gagal.\n" + e);
        }
        
        int id = Integer.parseInt(fieldIDCS.getText());
        int idProperty = Integer.parseInt(fieldIDProperty.getText());
        int jumlah = Integer.parseInt(fieldJumlah.getText());
        int idcsp = Integer.parseInt(fieldIDCSP.getText());
        
        try {
            String sql1 = "UPDATE coworking_space_properties SET id_cs=?, id_property=?, jumlah=? WHERE id_csp=?";
            PreparedStatement pst = conn.prepareStatement(sql1);
            pst.setInt(1, id);
            pst.setInt(2, idProperty);
            pst.setInt(3, jumlah);
            pst.setInt(4, idcsp);
            pst.executeUpdate();
            DisplayPropertyCS(idcsp);
            JOptionPane.showMessageDialog(null, "Data Properti Coworking Space Berhasil Diupdate!");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Koneksi ke Database Gagal.\n" + e);
        }
    }//GEN-LAST:event_buttonUpdateActionPerformed

